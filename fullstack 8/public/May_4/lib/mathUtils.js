/**
 * method for adding two values.
 * @param firstVal The parameter
 * @param secondVal The second parameter
 */
function addingValues(firstVal, secondVal) {
	var result = 0;
	result = firstVal + secondVal;
	return result;
}


/**
 * method for Substracting two values.
 * @param firstVal The parameter
 * @param secondVal The second parameter
 */
function subValues(firstVal, secondVal) {
	var result = 0;
	result = firstVal - secondVal;
	return result;
}


/**
 * method for Diviton two values.
 * @param firstVal The parameter
 * @param secondVal The second parameter
 */
function divValues(firstVal, secondVal) {
	var result = 0;
	result = firstVal / secondVal;
	return result;
}


/**
 * method for Multiplicaton two values.
 * @param firstVal The parameter
 * @param secondVal The second parameter
 */
function mulValues(firstVal, secondVal) {
	var result = 0;
	result = firstVal * secondVal;
	return result;
}
